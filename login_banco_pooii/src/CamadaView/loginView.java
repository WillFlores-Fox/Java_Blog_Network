    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        login objlogindto = new login();

        String usuario, senha;

        usuario = usuariotxt.getText();
        senha = senhatxt.getText();

        objlogindto.setUsuario(usuario);
        objlogindto.setSenha(senha);

        Conexao_login objlogindao = new Conexao_login();

        limparCampos();

        if (objlogindao.entrar(objlogindto) == true) {
            usuarioLogado.setUsuario(objlogindto);
            
            Tela_principal tela = new Tela_principal();
            tela.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_loginActionPerformed

